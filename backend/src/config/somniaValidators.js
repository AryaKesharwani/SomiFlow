/**
 * Get validator by address
 * @param {string} address - Validator address
 * @returns {Object|null} Validator object or null if not found
 */
export function getValidatorByAddress(address) {
  return SOMNIA_VALIDATORS.find(
    (v) => v.address.toLowerCase() === address.toLowerCase()
  ) || null;
}

/**
 * Get validator by name
 * @param {string} name - Validator name
 * @returns {Object|null} Validator object or null if not found
 */
export function getValidatorByName(name) {
  return SOMNIA_VALIDATORS.find(
    (v) => v.name.toLowerCase() === name.toLowerCase()
  ) || null;
}

/**
 * Get all validator addresses
 * @returns {string[]} Array of validator addresses
 */
export function getAllValidatorAddresses() {
  return SOMNIA_VALIDATORS.map((v) => v.address);
}

/**
 * Get all validators
 * @returns {Array} Array of all validators
 */
export function getAllValidators() {
  return SOMNIA_VALIDATORS;
}

